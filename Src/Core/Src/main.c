typedef enum {
    WAIT_HEAD1 = 0,
    WAIT_HEAD2,
    WAIT_HEAD3,
    WAIT_LEN,
    WAIT_PAYLOAD,
    WAIT_CRC
} RXSTATE;


void mspTxProc(uint8_t cmd, uint8_t *payload, uint8_t payloadlen)
{
    HAL_UART_Transmit(&huart1, (uint8_t*)"$M<", 3, 100);
    HAL_UART_Transmit(&huart1, &payloadlen, 1, 100);
    HAL_UART_Transmit(&huart1, &cmd, 1, 100);
    HAL_UART_Transmit(&huart1, payload, payloadlen, 100);
    uint8_t sum = payloadlen ^ cmd;
    for(int x=0; x<payloadlen; x++){
        sum ^= payload[x];
    }
    HAL_UART_Transmit(&huart1, &sum, 1, 100);
}

uint16_t mspRxProc(uint8_t *cmddata)
{
    static RXSTATE mspState = WAIT_HEAD1;
    static int16_t count = 0;
    static int16_t len = 0;
    static uint8_t sum = 0;

    int ret = 0;
    uint8_t rxdata;

    if (HAL_OK == HAL_UART_Receive(&huart1, &rxdata, 1, 1)){
        switch(mspState){
            case WAIT_HEAD1:
                if(rxdata == '$'){
                    mspState = WAIT_HEAD2;
                }
                break;
            case WAIT_HEAD2:
                if(rxdata == 'M'){
                    mspState = WAIT_HEAD3;
                }else{
                    mspState = WAIT_HEAD1;
                }
                break;
            case WAIT_HEAD3:
                if(rxdata == '>'){
                    sum = 0;
                    mspState = WAIT_LEN;
                }else{
                    mspState = WAIT_HEAD1;
                }
                break;
            case WAIT_LEN:
                len = rxdata;
                count = 0;
                sum ^= rxdata;
                mspState = WAIT_PAYLOAD;
                break;
            case WAIT_PAYLOAD:
                cmddata[count++] = rxdata;
                sum ^= rxdata;
                if (count == len +1){
                    mspState = WAIT_CRC;
                }
                break;
            case WAIT_CRC:
                if(sum == rxdata){
                    ret = len;
                }
                mspState = WAIT_HEAD1;
                break;
        }
    }
    return ret;
}

